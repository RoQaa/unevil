@app.post("/analyze-text")
def analyze_text(data: TextRequest):
    try:
        text = data.text.strip()

        if not text:
            raise HTTPException(status_code=400, detail="No text provided")

        lower_text = text.lower()

        # -------------------------
        # Basic metrics
        # -------------------------
        words = text.split()
        word_count = len(words)

        sentences = re.split(r'[.!؟?]+', text)
        sentence_count = len([s for s in sentences if s.strip()])

        avg_sentence_length = word_count / sentence_count if sentence_count else 0

        unique_words = len(set(words))
        lexical_diversity = unique_words / word_count if word_count else 0

        punctuation_count = text.count(",") + text.count("،")

        # -------------------------
        # AI Indicators
        # -------------------------
        ai_keywords = [
            "as an ai",
            "language model",
            "generated by ai",
            "chatgpt",
            "openai",
        ]

        formal_phrases = [
            "in conclusion",
            "moreover",
            "furthermore",
            "therefore",
            "additionally",
            "it is important to note",
            "في الختام",
            "علاوة على ذلك",
        ]

        score = 50
        reason = []

        # -------------------------
        # Rules
        # -------------------------

        # 1) Explicit AI words
        if any(k in lower_text for k in ai_keywords):
            return {
                "result": "Likely AI Generated",
                "confidence": "98%",
                "reason": "Explicit AI-related wording detected."
            }

        # 2) Very structured text
        if avg_sentence_length > 18:
            score += 10
            reason.append("Long structured sentences")

        # 3) Low lexical diversity (AI بيكرر كلمات)
        if lexical_diversity < 0.45:
            score += 10
            reason.append("Low vocabulary diversity")

        # 4) Formal connectors
        if any(p in lower_text for p in formal_phrases):
            score += 8
            reason.append("Formal structured phrasing")

        # 5) Over punctuation
        if punctuation_count > 6:
            score += 5
            reason.append("Heavy punctuation usage")

        # 6) Long text
        if word_count > 120:
            score += 10
            reason.append("Long consistent structure")

        # 7) Short human-like text
        if word_count < 15:
            score -= 15
            reason.append("Short natural text")

        # Clamp score
        score = max(0, min(score, 100))

        # -------------------------
        # Final decision
        # -------------------------
        if score >= 70:
            result = "Likely AI Generated"
        else:
            result = "Likely Human Written"

        return {
            "result": result,
            "confidence": f"{score}%",
            "reason": ", ".join(reason) if reason else "Natural writing style detected.",
            "details": {
                "word_count": word_count,
                "sentence_count": sentence_count,
                "avg_sentence_length": round(avg_sentence_length, 2),
                "lexical_diversity": round(lexical_diversity, 2)
            }
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))



