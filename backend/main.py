@app.post("/analyze-audio")
async def analyze_audio(file: UploadFile = File(...)):
    try:
        # ===== حفظ الملف =====
        with tempfile.NamedTemporaryFile(delete=False) as temp:
            temp.write(await file.read())
            temp_path = temp.name
        # ===== تأكد وجود البرنامج =====
        if which("ffmpeg") is None:
            raise Exception("FFmpeg not found, please install it")
        # ===== تحويل لـ WAV =====
        audio = AudioSegment.from_file(temp_path)
        wav_path = temp_path + ".wav"
        audio.export(wav_path, format="wav")

        # ===== Speech to Text =====
        transcript = speech_to_text(wav_path)

        # ===== Feature Extraction =====
        features = extract_audio_features(wav_path)

        # ===== AI Detection =====
        pred, confidence = predict_audio_ai(features)

        # ===== Response =====
        return {
            "transcript": transcript if transcript else "No speech detected",
            "audio_analysis": {
                "is_ai": bool(pred) if pred != "unknown" else "unknown",
                "confidence": f"{confidence * 100:.2f}%"
            }
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

    finally:
        # تنظيف الملفات
        try:
            if os.path.exists(temp_path):
                os.remove(temp_path)
            if os.path.exists(temp_path + ".wav"):
                os.remove(temp_path + ".wav")
        except:
            pass