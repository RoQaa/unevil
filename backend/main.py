def perform_text_analysis(text: str):
    lower_text = text.lower()

    # =========================
    # BASIC METRICS
    # =========================
    words = text.split()
    word_count = len(words)

    sentences = re.split(r'[.!؟?]+', text)
    sentences = [s.strip() for s in sentences if s.strip()]
    sentence_count = len(sentences)

    avg_sentence_length = word_count / sentence_count if sentence_count else 0

    unique_words = len(set(words))
    lexical_diversity = unique_words / word_count if word_count else 0

    punctuation_count = len(re.findall(r"[.,،;:!?؟]", text))

    # repetition (مهم جدًا)
    repetition_ratio = 1 - lexical_diversity

    # =========================
    # AI INDICATORS
    # =========================
    ai_keywords = [
        "as an ai",
        "language model",
        "generated by ai",
        "chatgpt",
        "openai",
        "artificial intelligence",
        "elevenlabs",
        "voice generated by",
        "ai generated",
        "ai-generated",
        "text to speech",
        "text-to-speech",
        "[name]",
        "[your name]",
        "<name>",
        "<your name>"
    ]

    formal_phrases = [
        "in conclusion",
        "moreover",
        "furthermore",
        "therefore",
        "additionally",
        "it is important to note",
        "في الختام",
        "علاوة على ذلك",
        "من المهم أن نلاحظ",
    ]

    # =========================
    # SCORING SYSTEM
    # =========================
    score = 50
    reasons = []

    # 🔴 1) Explicit AI
    if any(k in lower_text for k in ai_keywords):
        return {
            "result": "Likely AI Generated",
            "confidence": "99%",
            "reason": "Explicit AI-related wording detected",
            "score": 99
        }

    # 🟠 2) Sentence uniformity (AI بيحب نفس الطول)
    sentence_lengths = [len(s.split()) for s in sentences]
    if len(sentence_lengths) > 2:
        variance = np.var(sentence_lengths)
        if variance < 15:
            score += 10
            reasons.append("Uniform sentence structure")

    # 🟠 3) Long structured text
    if avg_sentence_length > 18:
        score += 10
        reasons.append("Long structured sentences")

    # 🟠 4) Repetition (مهم جدًا)
    if repetition_ratio > 0.55:
        score += 12
        reasons.append("High word repetition")

    # 🟠 5) Low lexical diversity
    if lexical_diversity < 0.45:
        score += 8
        reasons.append("Low vocabulary diversity")

    # 🟠 6) Formal connectors
    if any(p in lower_text for p in formal_phrases):
        score += 8
        reasons.append("Formal structured phrasing")

    # 🟠 7) Too clean punctuation
    if punctuation_count > 8:
        score += 5
        reasons.append("Overly consistent punctuation")

    # 🟠 8) Very long text
    if word_count > 150:
        score += 10
        reasons.append("Long consistent structure")

    # 🟢 9) Human indicators
    if word_count < 20:
        score -= 15
        reasons.append("Short natural text")

    if "..." in text or "؟" in text:
        score -= 5
        reasons.append("Casual human punctuation")

    # =========================
    # NORMALIZATION
    # =========================
    score = max(0, min(score, 100))

    # =========================
    # FINAL RESULT
    # =========================
    if score >= 65:
        result = "Likely AI Generated"
    else:
        result = "Likely Human Written"

    return {
        "result": result,
        "confidence": f"{score}%",
        "reason": ", ".join(reasons) if reasons else "Natural writing style detected",
        "score": score,
        "details": {
            "word_count": word_count,
            "sentence_count": sentence_count,
            "avg_sentence_length": round(avg_sentence_length, 2),
            "lexical_diversity": round(lexical_diversity, 2),
            "repetition_ratio": round(repetition_ratio, 2)
        }
    }


@app.post("/analyze-text")
def analyze_text(data: TextRequest):
    try:
        text = data.text.strip()

        if not text:
            raise HTTPException(status_code=400, detail="No text provided")

        analysis = perform_text_analysis(text)
        
        # Remove internal score from response to match original API signature
        if "score" in analysis:
            del analysis["score"]
            
        return analysis

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

