@app.post("/analyze-image")
def analyze_image(file: UploadFile = File(...)):
    try:
        image_bytes = file.file.read()

        response = requests.post(
            "https://api.sightengine.com/1.0/check.json",
            data={
                "models": "genai",
                "api_user": SIGHTENGINE_API_USER,
                "api_secret": SIGHTENGINE_API_SECRET,
            },
            files={
                "media": ("image.jpg", image_bytes, file.content_type)
            },
            timeout=60,
        )

        response.raise_for_status()
        result_json = response.json()

        ai_score = float(result_json.get("type", {}).get("ai_generated", 0.0)) * 100

        return {
            "ai_score": ai_score,
            "raw": result_json
        }

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))
    
    





# تحميل موديل Vosk
vosk_model = Model("vosk-model-small-en-us-0.15")

# تحميل موديل الصوت (لو موجود)
AUDIO_MODEL_PATH = "audio_model.pkl"
audio_model = None

if os.path.exists(AUDIO_MODEL_PATH):
    audio_model = joblib.load(AUDIO_MODEL_PATH)


# ================= AUDIO FEATURES =================
def extract_audio_features(file_path):
    y, sr = librosa.load(file_path, sr=None)

    mfcc = np.mean(librosa.feature.mfcc(y=y, sr=sr), axis=1)
    spectral_centroid = np.mean(librosa.feature.spectral_centroid(y=y, sr=sr))
    zero_crossing = np.mean(librosa.feature.zero_crossing_rate(y))

    features = np.concatenate([mfcc, [spectral_centroid, zero_crossing]])
    return features


# ================= AUDIO AI DETECTION =================
def predict_audio_ai(features):
    if audio_model is None:
        return "unknown", 0.0

    pred = audio_model.predict([features])[0]
    prob = audio_model.predict_proba([features])[0]

    return int(pred), float(np.max(prob))


# ================= SPEECH TO TEXT =================
def speech_to_text(wav_path):
    wf = wave.open(wav_path, "rb")
    rec = KaldiRecognizer(vosk_model, wf.getframerate())

    text = ""

    while True:
        data = wf.readframes(4000)
        if len(data) == 0:
            break
        if rec.AcceptWaveform(data):
            res = json.loads(rec.Result())
            text += " " + res.get("text", "")

    final_res = json.loads(rec.FinalResult())
    text += " " + final_res.get("text", "")

    return text.strip()


