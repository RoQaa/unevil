HEADERS = ["Sample", "Lang", "Human / AI", "% Confidence", "Explanation"]

def set_cell_bg(cell, hex_color):
    tc   = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd  = OxmlElement('w:shd')
    shd.set(qn('w:val'),   'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'),  hex_color)
    tcPr.append(shd)

def style_header_row(row, bg_hex):
    for cell in row.cells:
        set_cell_bg(cell, bg_hex)
        for para in cell.paragraphs:
            para.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in para.runs:
                run.bold = True
                run.font.size = Pt(10)
                run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)

def add_data_row(table, values, shade_hex=None):
    row = table.add_row()
    for i, val in enumerate(values):
        cell = row.cells[i]
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
        para = cell.paragraphs[0]
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run  = para.add_run(str(val))
        run.font.size = Pt(9)
        if i == 3:   # confidence column
            try:
                pct = float(str(val).replace('%','').strip())
                if pct >= 85:
                    run.font.color.rgb = RGBColor(0x2E, 0x7D, 0x32); run.bold = True
                elif pct >= 60:
                    run.font.color.rgb = RGBColor(0xF5, 0x7F, 0x17); run.bold = True
                else:
                    run.font.color.rgb = RGBColor(0xC6, 0x28, 0x28); run.bold = True
            except:
                pass
        if shade_hex:
            set_cell_bg(cell, shade_hex)

def add_section(doc, title, header_bg, alt_bg, rows_data):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(14)
    p.paragraph_format.space_after  = Pt(4)
    r = p.add_run(f"  {title}  ")
    r.bold = True
    r.font.size = Pt(13)
    r.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
    pPr = p._p.get_or_add_pPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'),   'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'),  header_bg)
    pPr.append(shd)

    tbl = doc.add_table(rows=1, cols=len(HEADERS))
    tbl.style = 'Table Grid'
    hdr = tbl.rows[0]
    for i, h in enumerate(HEADERS):
        hdr.cells[i].text = h
    style_header_row(hdr, header_bg)

    for idx, row_vals in enumerate(rows_data):
        shade = alt_bg if idx % 2 == 1 else None
        add_data_row(tbl, row_vals, shade)

    doc.add_paragraph()

