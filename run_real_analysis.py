def detect_text(text: str):
    words     = text.split()
    sentences = [s.strip() for s in re.split(r'[.!؟?]+', text) if s.strip()]
    wc  = len(words)
    sc  = len(sentences)
    avg = wc / sc if sc else 0
    lex = len(set(words)) / wc if wc else 0
    rep = 1 - lex
    lower = text.lower()

    ai_kw = ["as an ai","language model","generated by ai","chatgpt","openai",
              "artificial intelligence","ai generated","ai-generated","text to speech"]
    formal = ["in conclusion","moreover","furthermore","therefore","additionally",
              "it is important to note","في الختام","علاوة على ذلك"]

    if any(k in lower for k in ai_kw):
        return "AI", 99, "Explicit AI-related wording detected"

    score   = 50
    reasons = []
    sl = [len(s.split()) for s in sentences]
    if len(sl) > 2 and np.var(sl) < 15:
        score += 10; reasons.append("Uniform sentence structure")
    if avg > 18:
        score += 10; reasons.append("Long structured sentences")
    if rep > 0.55:
        score += 12; reasons.append("High word repetition")
    if lex < 0.45:
        score +=  8; reasons.append("Low vocabulary diversity")
    if any(p in lower for p in formal):
        score +=  8; reasons.append("Formal structured phrasing")
    if wc > 150:
        score += 10; reasons.append("Long consistent structure")
    if wc < 20:
        score -= 15; reasons.append("Short natural text")
    if "..." in text or "؟" in text:
        score -=  5; reasons.append("Casual human punctuation")

    score = max(0, min(score, 100))
    label = "AI" if score >= 65 else "Human"
    reason = ", ".join(reasons) if reasons else "Natural writing style detected"
    return label, score, reason


def detect_image_bytes(image_bytes, filename="image.jpg", content_type="image/jpeg"):
    r = requests.post(
        "https://api.sightengine.com/1.0/check.json",
        data={"models": "genai", "api_user": SIGHTENGINE_USER, "api_secret": SIGHTENGINE_SECRET},
        files={"media": (filename, image_bytes, content_type)},
        timeout=60,
    )
    r.raise_for_status()
    js = r.json()
    ai_score = float(js.get("type", {}).get("ai_generated", 0.0)) * 100
    label  = "AI" if ai_score >= 50 else "Human"
    reason = "Visual GenAI indicators detected" if ai_score >= 50 else "Natural visual patterns detected"
    return label, round(ai_score, 2), reason


def detect_video(video_path):
    import cv2
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        return "N/A", 0, "Failed to open video"
    total = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    if total > 0:
        cap.set(cv2.CAP_PROP_POS_FRAMES, total // 2)
    ret, frame = cap.read()
    cap.release()
    if not ret:
        return "N/A", 0, "Failed to extract frame"
    ret2, buf = cv2.imencode('.jpg', frame)
    if not ret2:
        return "N/A", 0, "Failed to encode frame"
    return detect_image_bytes(buf.tobytes(), "frame.jpg", "image/jpeg")


def detect_audio(audio_path):
    if hf_model is None:
        return "N/A", 0, "Model not loaded"
    try:
        audio_seg = AudioSegment.from_file(audio_path)
        if len(audio_seg) > 30_000:
            audio_seg = audio_seg[:30_000]
        with tempfile.NamedTemporaryFile(delete=False, suffix=".wav") as tmp:
            wav_path = tmp.name
        audio_seg.export(wav_path, format="wav")

        audio_data, _ = librosa.load(wav_path, sr=16000)
        inputs = hf_fe(audio_data, sampling_rate=16000, return_tensors="pt", padding=True)
        with torch.no_grad():
            logits = hf_model(**inputs).logits
        pred_id = torch.argmax(logits, dim=-1).item()
        conf    = torch.softmax(logits, dim=-1)[0][pred_id].item()
        raw_label = hf_model.config.id2label[pred_id].lower()

        is_ai = any(k in raw_label for k in ["fake", "spoof", "ai", "generated"])
        label  = "AI" if is_ai else "Human"
        reason = f"Model label: '{hf_model.config.id2label[pred_id]}'"
        os.remove(wav_path)
        return label, round(conf * 100, 2), reason
    except Exception as e:
        return "N/A", 0, str(e)


